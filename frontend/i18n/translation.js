const MACHINE_TRANSLATION_LANGUAGE_OVERRIDES = {
  "nb-no": "no",
  "sq-al": "sq",
  "zh-cn": "zh-CN",
};

export function normalizeLanguageCode(language) {
  return String(language || "en").trim();
}

export function getBaseLanguageCode(language) {
  return normalizeLanguageCode(language).toLowerCase().split("-")[0] || "en";
}

export function getMachineTranslationLanguageCode(language) {
  const normalizedLanguage = normalizeLanguageCode(language).toLowerCase();

  return (
    MACHINE_TRANSLATION_LANGUAGE_OVERRIDES[normalizedLanguage] ||
    getBaseLanguageCode(normalizedLanguage)
  );
}

